  def factorial(n: Int): Int = {
    if (n > 0) n * factorial(n - 1)
    else 1
  }

  println("Factorial of 5 = " + factorial(5))

  // -------------------------------------------------------------------------------------------------------------------
  // 3 Exercise: fibonacci
  // -------------------------------------------------------------------------------------------------------------------

  def getNFibonacci(n: Int): Int = {
    if (n <= 2) 1
    else getNFibonacci(n - 1) + getNFibonacci(n - 2)
  }

  def fibonacci(n: Int) = {
    def serie(index: Int): String = {
      if (index <= n) getNFibonacci(index) + ", " + serie(index + 1)
      else ""
    }

    serie(1)
  }

  println(fibonacci(8))

